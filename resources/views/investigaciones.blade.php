<h5>PUBLICACIONES</h5>
<p class="light" align="justify">Manual de Conservación biológica en viñedos: conceptos clave y actividades prácticas</p>
<p class="light" align="justify">Este manual ha sido confeccionado por el equipo del Programa Vino, Cambio Climático y Biodiversidad del Instituto de Ecología y Biodiversidad y de la Universidad Austral de Chile.</p>
<p class="light" align="justify">Su contenido se basa en investigaciones hechas en Chile, y está pensado para ser un libro de terreno, de inspiración y consulta, de modo de llevar a la práctica la conservación biológica de nuestros ecosistemas. Tiene un diseño que permite llevarlo a todas partes, tenerlo a mano, en la camioneta; que se caiga y no se rompa: un compañero de ruta diaria. </p>
<p class="light" align="justify">Está dirigido a administradores de campo, a planificadores y diseñadores de paisajes ecoproductivos. Y por supuesto, a toda persona amante de la naturaleza, que busque armonizar la conservación con la producción a escala comercial.</p>
