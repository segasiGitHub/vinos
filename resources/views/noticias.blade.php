            <h5>Guardianas de biodiversidad: Viñas miembro del VCCB son premiadas por prestigiosa revista británica </h5>

            <p class="light" align="justify">Esta semana en medio de la Feria del Vino de Londres, dos viñas chilenas miembro del <a href="#test5">Programa Vino, Cambio Climático y Biodiversidad</a>, fueron reconocidas en distintas categorías en los <a href="https://www.thedrinksbusiness.com/awards/green-awards-2017/">DB Green Awards 2017</a>.</p>
            <p class="light" align="justify">La revista británica <a href="https://www.thedrinksbusiness.com/">The Drink Business</a>, cada año otorga premios a aquellas viñas que destacan en diversos ámbitos vinculados a la protección del <b>medio ambiente</b>. Esta Iniciativa busca crear conciencia sobre los problemas <b>ecológicos</b> en el comercio de bebidas, reconocer y recompensar a aquellos que lideran el camino en la <b>sostenibilidad</b> y el buen desempeño ambiental.</p>
            <p class="light" align="justify">Segundo lugar bajo la categoría manejo del <b>agua</b>, y la nominación en categoría biodiversidad: <a href="http://vsptwinegroup.com/">VSPT Wine Group</a> obtuvo dos reconocimientos por su activo compromiso con el medio ambiente. Desde el año 2014 cuentan con un área especial para la conservación de flora y fauna nativa en sus campos, según explica Josefa Morales, Encargada de Sustentabilidad de VSPT, “con el objetivo de minimizar el impacto de la actividad agrícola en la biodiversidad que alberga sus viñedos”. En 2016 implementan el control de especies exóticas invasoras, y elaboración de un masterplan de biodiversidad, que en su primera etapa incluye la incorporación de 3.500 árboles y 1.000 plantas, todas especies nativas.</p>
            <p class="light" align="justify">Por su parte, <a href="http://www.conosur.com/">Viña Cono Sur</a> fue galardonada con el segundo lugar en la categoría <b>Biodiversidad</b>. “Este reconocimiento es una tremenda alegría para nosotros ya que resume de gran manera el trabajo dedicado y permanente que realizamos cada día en pro del medio ambiente, y de manera muy particular, con respecto al cuidado y promoción de la biodiversidad en nuestros campos”, destacó el enólogo y Gerente General de la viña, Adolfo Hurtado.</p>
            <p class="light" align="justify">Cono Sur también ha implementado distintas acciones que promueven la conservación dentro de sus viñedos, tanto la <b>agricultura</b> sostenible como la gestión ecológica se han convertido en un sello de su actividad productiva. “En un país con condiciones naturales extraordinarias para la producción de uvas y vinos, debemos darnos cuenta que los temas medio ambientales y de sostenibilidad, como lo es por ejemplo la promoción de la biodiversidad en nuestros campos, son un activo de valor incalculable que debemos saber desarrollar y explotar de forma tal que representen un verdadero compromiso para cada uno de nosotros y así transformarlos en verdadera ventaja para nuestra de vinos en Chile”, comentó el Gerente de Cono Sur.</p>
            <p class="light" align="justify">Entre sus mejores iniciativas, se encuentran algunas acciones de conservación para destacar: </p>
            <p class="light" align="justify">1.-Fomentar la recolonización de aves depredadora en los viñedos y evitar el uso de veneno para ratones.</p>
            <p class="light" align="justify">2.-Crear y mantener corredores de vegetación nativa conectados en diferentes escalas y enriquecer con especies nativas los corredores que mantienen insectos beneficiosos a lo largo de nuestro viñedo.</p>
            <p class="light" align="justify">3.-Exclusión del ganado doméstico grande de las colinas y los barrancos.</p>
            <p class="light" align="justify">4.-Reforestación y reintroducción de flora nativa en nuestras granjas</p>
            <p class="light" align="justify">5.-Planificación y diseño de nuevos viñedos, preservando y asignando áreas de refugios de biodiversidad</p>
            <p class="light" align="justify">En palabras de Olga Barbosa, directora del Programa Vino Cambio, Climático y Biodiversidad, “esta es una tremenda oportunidad para hablar de cierta parte de la industria chilena que está volcando su mirada hacia la conservación biológica. Algo que para muchos suena irrisorio, el sector vitivinícola lo está desmitificando: <b>el desarrollo y la protección de servicios ecosistémicos sí son compatibles”</b>, enfatizó la investigadora del <a href="http://www.ieb-chile.cl/2017/">Instituto de Ecología y Biodiversidad</a>.</p>
